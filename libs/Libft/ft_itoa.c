#include "libft.h"

char	*ft_itoa(int n)
{
	char	*res;
	int		ln;
	int		t;

	ln = (n <= 0) ? 1 : 0;
	t = n;
	while (t != 0)
	{
		t /= 10;
		ln++;
	}
	if (!(res = malloc(ln + 1)))
		return (0);
	if (n == 0)
		res[0] = '0';
	res[ln] = 0;
	if (n < 0)
		res[0] = '-';
	while (n != 0)
	{
		ln--;
		res[ln] = '0' + (n % 10 < 0 ? -(n % 10) : n % 10);
		n /= 10;
	}
	return (res);
}
