#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		len;

	len = ft_strlen(s1);
	if (!(s2 = malloc(len + 1)))
		return (0);
	ft_strlcpy(s2, s1, len + 1);
	return (s2);
}
