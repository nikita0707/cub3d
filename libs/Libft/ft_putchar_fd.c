#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	unsigned char i;

	if (c >= 0)
		write(fd, &c, 1);
	else
	{
		i = (c >= -64) ? 195 : 194;
		write(fd, &i, 1);
		i = c + ((c >= -64) ? 192 : 256);
		write(fd, &i, 1);
	}
}
