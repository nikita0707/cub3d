#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		ln;
	t_list	*temp;

	ln = 0;
	temp = lst;
	while (temp)
	{
		ln++;
		temp = temp->next;
	}
	return (ln);
}
